let skor = 0;
let mode = "";
let selectedItem1 = null;
let selectedItem2 = null;
let matchedPairsCount = 0; // Menghitung pasangan yang sudah ditemukan di ronde ini
let currentRoundData = []; // Data untuk ronde saat ini (4 teman)

